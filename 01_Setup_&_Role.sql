--  Data_Masking (Masking the email and phone number from the role)


USE SCHEMA SAAS_PROD.UTILITY;

CREATE MASKING POLICY IF NOT EXISTS EMAIL_MASK AS (val STRING )
RETURNS STRING ->
CASE WHEN CURRENT_ROLE() IN ('LOADER_ROLE','TRANSFORMER_ROLE', 'ACCOUNTADMIN') THEN val
else REGEXP_REPLACE(val, '.+@', '****@')
END;


CREATE MASKING POLICY IF NOT EXISTS PHONE_MASKE AS (val STRING)
RETURNS STRING ->
CASE WHEN CURRENT_ROLE() IN ('LOADER_ROLE','TRANSFORMER_ROLE', 'ACCOUNTADMIN') THEN val
else CONCAT('-***-***', RIGHT(val,4))
END;



