USE ROLE ACCOUNTADMIN;


CREATE ROLE IF NOT EXISTS LOADER_ROLE
COMMENT='SERVICE ACCOUNT ROLE';

CREATE ROLE IF NOT EXISTS TRANSFORMER_ROLE
COMMENT='DBT TRANSFROMATION';

CREATE ROLE IF NOT EXISTS BI_ROLE
    COMMENT = 'Power BI service principal - read-only on MARTS';

CREATE ROLE IF NOT EXISTS DEVLOPER_ROLE
    COMMENT = 'Engineers - read all, write to DEV only';

CREATE ROLE IF NOT EXISTS ANALYST_ROLE
    COMMENT = 'Business analysts - read MARTS and INTERMEDIATE';


GRANT ROLE LOADER_ROLE TO ROLE SYSADMIN;
GRANT ROLE TRANSFORMER_ROLE TO ROLE SYSADMIN;
GRANT ROLE BI_ROLE TO ROLE SYSADMIN;
GRANT ROLE DEVLOPER_ROLE TO ROLE SYSADMIN;
GRANT ROLE ANALYST_ROLE TO ROLE SYSADMIN;

GRANT ROLE TRANSFORMER_ROLE TO USER UPRETI99;



CREATE WAREHOUSE IF NOT EXISTS LOADING_WH
WAREHOUSE_SIZE='SMALL'
AUTO_SUSPEND=60
AUTO_RESUME=TRUE
INITIALLY_SUSPENDED=TRUE
COMMENT = 'Dedicated compute for S3 data ingestion via COPY INTO';

CREATE WAREHOUSE IF NOT EXISTS TRANSFORMING_WH
    WAREHOUSE_SIZE = 'SMALL'
    AUTO_SUSPEND = 120
    AUTO_RESUME = TRUE
    INITIALLY_SUSPENDED = TRUE
    COMMENT = 'Dedicated compute for dbt transformations';

CREATE WAREHOUSE IF NOT EXISTS BI_WH
    WAREHOUSE_SIZE = 'SMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE
    INITIALLY_SUSPENDED = TRUE
    COMMENT = 'Dedicated compute for Power BI queries';

CREATE WAREHOUSE IF NOT EXISTS DEV_WH
    WAREHOUSE_SIZE = 'XSMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE
    INITIALLY_SUSPENDED = TRUE
    COMMENT = 'Developer ad-hoc queries and testing';



GRANT USAGE ON WAREHOUSE LOADING_WH TO ROLE LOADER_ROLE;
GRANT USAGE ON WAREHOUSE TRANSFORMING_WH TO ROLE TRANSFORMER_ROLE;
GRANT USAGE ON WAREHOUSE BI_WH TO ROLE BI_ROLE;
GRANT USAGE ON WAREHOUSE DEV_WH TO ROLE DEVLOPER_ROLE;
GRANT USAGE ON WAREHOUSE DEV_WH TO ROLE ANALYST_ROLE;



CREATE RESOURCE MONITOR IF NOT EXISTS LOADING_MONITOR
WITH CREDIT_QUOTA = 100
FREQUENCY = MONTHLY
START_TIMESTAMP = IMMEDIATELY
TRIGGERS
        ON 75 PERCENT DO NOTIFY
        ON 90 PERCENT DO NOTIFY
        ON 100 PERCENT DO SUSPEND;


CREATE RESOURCE MONITOR IF NOT EXISTS TRANSFORM_MONTIOR
WITH CREDIT_QUOTA = 200
FREQUENCY = MONTHLY
START_TIMESTAMP = IMMEDIATELY
TRIGGERS 
        ON 75 PERCENT DO NOTIFY
        ON 90 PERCENT DO NOTIFY
        ON 100 PERCENT DO SUSPEND;


ALTER WAREHOUSE LOADING_WH SET RESOURCE_MONITOR =  LOADING_MONITOR;
ALTER WAREHOUSE TRANSFORMING_WH SET RESOURCE_MONITOR = TRANSFORM_MONTIOR;


CREATE DATABASE IF NOT EXISTS SAAS_PROD
    DATA_RETENTION_TIME_IN_DAYS = 7
    COMMENT = 'Production environment';

CREATE DATABASE IF NOT EXISTS SAAS_DEV
DATA_RETENTION_TIME_IN_DAYS = 1
COMMENT = 'Development environment - cloned from PROD weekly';


use database SAAS_PROD;

CREATE SCHEMA IF NOT EXISTS RAW
    WITH MANAGED ACCESS
    DATA_RETENTION_TIME_IN_DAYS = 14
    COMMENT = 'Landing zone - immutable source data from S3';

CREATE SCHEMA IF NOT EXISTS STAGING
    WITH MANAGED ACCESS
    DATA_RETENTION_TIME_IN_DAYS = 7
    COMMENT = 'Cleaned and typed data - 1:1 with source tables';

CREATE SCHEMA IF NOT EXISTS INTERMEDIATE
    WITH MANAGED ACCESS
    DATA_RETENTION_TIME_IN_DAYS = 7
    COMMENT = 'Cross-source joins and business logic';

CREATE SCHEMA IF NOT EXISTS MARTS
    WITH MANAGED ACCESS
    DATA_RETENTION_TIME_IN_DAYS = 14
    COMMENT = 'Final dimensional models - Power BI connects here';

CREATE SCHEMA IF NOT EXISTS UTILITY
    WITH MANAGED ACCESS
    COMMENT = 'Pipeline metadata, audit logs, helper functions';


