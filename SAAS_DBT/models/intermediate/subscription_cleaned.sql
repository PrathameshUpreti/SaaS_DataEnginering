SELECT SUBSCRIPTION_ID,  USER_ID,
INITCAP(TRIM(PLAN_NAME)) AS PLAN_NAME,
UPPER(TRIM(PLAN_TIER)) AS PLAN_TIER,
MONTHLY_PRICE, START_DATE,
END_DATE,
CASE
    WHEN UPPER(TRIM(STATUS)) = 'ACTIVE' AND END_DATE < CURRENT_DATE() THEN 'EXPIRED'
    ELSE UPPER(TRIM(STATUS))
END AS STATUS,
UPPER(TRIM(BILLING_CYCLE)) AS BILLING_CYCLE,
LOADED_AT,
UPDATED_AT

FROM {{ source('staging', 'STG_SUBSCRIPTIONS') }}

WHERE SUBSCRIPTION_ID IS NOT NULL 
AND START_DATE <= COALESCE(END_DATE, CURRENT_DATE())
