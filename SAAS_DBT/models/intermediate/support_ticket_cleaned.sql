-- Cleaned support ticket data with status correction for missing resolution dates

{{ config(materialized='view') }}

SELECT TICKET_ID, USER_ID,
TRIM(SUBJECT) AS SUBJECT,
UPPER(TRIM(PRIORITY)) AS PRIORITY,
CASE
    WHEN REPLACE(UPPER(TRIM(STATUS)), ' ', '_') = 'RESOLVED' AND RESOLVED_AT IS NULL THEN 'IN_PROGRESS'
    ELSE REPLACE(UPPER(TRIM(STATUS)), ' ', '_')
END AS STATUS,
UPPER(TRIM(CATEGORY)) AS CATEGORY,
CREATED_AT,RESOLVED_AT,AGENT_ID,
CASE
    WHEN SATISFACTION_SCORE BETWEEN 1 AND 5 THEN ROUND(SATISFACTION_SCORE)::INTEGER
        ELSE NULL
END AS SATISFACTION_SCORE,
LOADED_AT,UPDATED_AT
FROM {{ source('staging', 'STG_SUPPORT_TICKETS') }}
WHERE TICKET_ID IS NOT NULL
