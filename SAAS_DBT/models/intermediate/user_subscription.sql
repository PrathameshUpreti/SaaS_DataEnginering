SELECT u.USER_ID,
u.EMAIL,
u.FIRST_NAME, u.LAST_NAME,
u.PHONE, u.SIGNUP_DATE,
u.STATUS AS USER_STATUS,
u.COUNTRY,
s.SUBSCRIPTION_ID,
COALESCE(s.PLAN_NAME, 'FREE') AS PLAN_NAME,
COALESCE(s.PLAN_TIER, 'FREE') AS PLAN_TIER,
COALESCE(s.MONTHLY_PRICE, 0) AS MONTHLY_PRICE,
s.START_DATE AS SUBSCRIPTION_START,
s.END_DATE AS SUBSCRIPTION_END,
COALESCE(s.STATUS, 'NOT_SUBSCRIBED') AS SUBSCRIPTION_STATUS,
COALESCE(s.BILLING_CYCLE, 'NONE') AS BILLING_CYCLE,
DATEDIFF('day', u.SIGNUP_DATE, s.START_DATE) AS DAYS_TO_FIRST_SUSCRIPTION

FROM {{ ref('user_cleaned') }} u
LEFT JOIN {{ ref('subscription_cleaned') }} s
    ON u.USER_ID = s.USER_ID